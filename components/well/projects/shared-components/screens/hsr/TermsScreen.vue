          this.loading = false
          this.getPortfolioNextStep()
        })
      this.setMode('register')
    },
    async updatePortfolioInfo() {},
    async optHsrForProject() {
      await this.$store.dispatch('project/optInHsr', {
        id: this.$store.state.project.project.id,
      })

      await this.$store.dispatch(
        'project/getProject',
        this.$store.state.project.project.id
      )
      // HSR agreement field
      await this.$store.dispatch('project/sendAgreement', {
        projectId: this.$store.state.project.project.id,
        type: 'v2-hsr',
      })
      await this.$store.dispatch(
        'project/getProjectInvoices',
        this.$store.state.project.project.id
      )
      this.setMode('invoice')
      this.setCurrentStep('3')
    },
    getPortfolioNextStep() {
      this.$store.commit('portfolio/setMode', 'register')
    },
  },
}
